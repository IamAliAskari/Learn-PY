# Else in For Loop   

# The else keyword in a for loop specifies a block of code to be executed when the loop is finished     !

my_friends = ['ali', 'reza', 'mmd', 'mohsen', 'javad']

for name in my_friends :

    print(name) 


else :

    print('+++++++++++++++++++++++++')
    
    print('Finished your list !')

    print('+++++++++++++++++++++++++')

