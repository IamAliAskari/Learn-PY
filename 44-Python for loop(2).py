#################################  Python for Loops  #################################

a_a = ['James', 'Connell', 'Haward','Danny'] 

for item in a_a :

    print(item) 

print('_____________________________________________')

# The break Statement  

# With the break statement we can stop the loop before it has looped through all the items    !

for x in a_a :

    print(x) 

    if x == 'Haward' :

        break

print('_____________________________________________') 


