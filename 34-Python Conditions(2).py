#################################  Python If ... Else  ################################# 

# The elif keyword is pythons way of saying    "if the previous conditions were not true, then try this condition"    ! 

x = 15 

y = 19 

if x == y :

    print('Ok  x  Equals  y')          # False

elif x < y :

    print('Ok  x  less than  y')       # True

elif x != y :

    print('Ok  x  Not Equals  y')      # False

print('---------------------------------------------------------------------') 

# The else keyword catches anything which isn't caught by the preceding conditions    !

i = 12 

j = 2 

if i == j :

    print('Ok  i  Equals  j') 

else : 

    print ('Error !  i  Not Equals  j') 