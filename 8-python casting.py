# Casting in Python

# Integer :          ########  if print type is value's : output  is  <class 'int'>  ########

x = int(3)              # x will be 3

i = int(4.8)            # i will be 4

n = int('3')            # n will be 3

print(x)

print('----------------------------')

print(i)

print('----------------------------')

print(n)

print('***********************************************************')

# Float :            ########  if print type is value's : output  is  <class 'float'>  ########

y = float(3.8)              # y will be 3.8

j = float(5)                # j will be 5.0

m = float('55')             # m will be 55.0

print(y)

print('----------------------------')

print(j)

print('----------------------------')

print(m)

print('***********************************************************')

# String :            ########  if print type is value's : output  is  <class 'str'>  ########

w = str(44)                 # w will be 44

q = str(12.6)               # q will be 12.6

s = str('47')               # s will be 47

print(w)

print('----------------------------')

print(q)

print('----------------------------')

print(s)






