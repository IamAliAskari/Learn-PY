# Nested Loops 

# A nested loop is a loop inside a loop    ! 

# The "inner loop" will be executed one time for each iteration of the "outer loop"     !

my_names = ['ali', 'connell', 'mmd'] 

my_lastname = ['askari', 'outis', 'musavi'] 

for first in my_names :

    for last in my_lastname :

        print((first, last)) 

        




