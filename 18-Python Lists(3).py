#################################  Python ---> Access List Items  #################################  

# List items are indexed and you can access them by referring to the index number  !

new_list = ['ali', 'outis','connell', 'askari','CNO','aliAskari','ConnellOutis'] 

print(new_list[2]) 

print('---------------------------------------------------------------------') 

# The search will start at index 2 (included) and end at index 5 (not included)  ! 

print(new_list[1:5]) 

print('---------------------------------------------------------------------')  

list_one = new_list[0:5] 

print(list_one)

print('---------------------------------------------------------------------') 

print(new_list[:3]) 

print('---------------------------------------------------------------------') 

print(new_list[1:]) 

print('---------------------------------------------------------------------') 

print(new_list[-1])  

print('---------------------------------------------------------------------') 

print(new_list[-5:-1])  

